// .substring() - Retorna parte da string informando o index inicial ou o index inicial e o final
//Artigo: https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/String/substring

const str = "Mozilla";

console.log(str.substring(2));
    //zilla
    // Retorna "zilla", pois ele irá pegar o caracter que está na posição 2 em diante

console.log(str.substring(1,4));
    //ozi
    // Retorna "ozi", pois ele irá pegar o caracter que está na posição 1 e o caractere da posição anterior ao 4